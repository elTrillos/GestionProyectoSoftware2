def wait(text):
    print(f"\n\n{text}", end="", flush=True)
    if platform.system() == "Windows":
        os.system("pause >nul")
    else:
        os.system("/bin/bash -c 'read -s -n 1 -p \"\"'")
    print()
    print("\n")


